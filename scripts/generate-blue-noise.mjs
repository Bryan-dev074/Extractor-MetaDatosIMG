/**
 * Generates the repository-owned 64x64 blue-noise rank permutation.
 *
 * Method: progressive toroidal farthest-point sampling. At every step the
 * unoccupied lattice point whose nearest occupied neighbor is farthest away is
 * selected; a fixed integer hash resolves ties. This script and its generated
 * data are released under CC0-1.0.
 *
 * Run explicitly with:
 *   node scripts/generate-blue-noise.mjs
 */
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const size = 64;
const area = size * size;
const ranks = new Uint16Array(area);
const occupied = new Uint8Array(area);
const nearestDistance = new Uint16Array(area);
nearestDistance.fill(0xffff);
let current = 17 * size + 37;

function tieHash(index) {
  let value = (index ^ 0x9e3779b9) >>> 0;
  value = Math.imul(value ^ (value >>> 16), 0x21f0aaad);
  value = Math.imul(value ^ (value >>> 15), 0x735a2d97);
  return (value ^ (value >>> 15)) >>> 0;
}

for (let rank = 0; rank < area; rank += 1) {
  ranks[current] = rank;
  occupied[current] = 1;
  const chosenX = current & (size - 1);
  const chosenY = current >>> 6;
  let next = -1;
  let bestDistance = -1;
  let bestTie = -1;

  for (let index = 0; index < area; index += 1) {
    if (occupied[index]) continue;
    const x = index & (size - 1);
    const y = index >>> 6;
    const directX = Math.abs(x - chosenX);
    const directY = Math.abs(y - chosenY);
    const dx = Math.min(directX, size - directX);
    const dy = Math.min(directY, size - directY);
    const distance = dx * dx + dy * dy;
    if (distance < nearestDistance[index]) nearestDistance[index] = distance;
    const candidateDistance = nearestDistance[index];
    const candidateTie = tieHash(index);
    if (
      candidateDistance > bestDistance ||
      (candidateDistance === bestDistance && candidateTie > bestTie)
    ) {
      next = index;
      bestDistance = candidateDistance;
      bestTie = candidateTie;
    }
  }
  if (rank + 1 < area) current = next;
}

const crcTable = new Uint32Array(256);
for (let value = 0; value < 256; value += 1) {
  let crc = value;
  for (let bit = 0; bit < 8; bit += 1) {
    crc = (crc >>> 1) ^ (crc & 1 ? 0xedb88320 : 0);
  }
  crcTable[value] = crc >>> 0;
}
let crc = 0xffffffff;
for (const rank of ranks) {
  for (const byte of [rank >>> 8, rank & 0xff]) {
    crc = (crc >>> 8) ^ crcTable[(crc ^ byte) & 0xff];
  }
}
const checksum = `crc32:${((crc ^ 0xffffffff) >>> 0).toString(16).padStart(8, "0")}`;
const rows = [];
for (let offset = 0; offset < ranks.length; offset += 32) {
  rows.push(`  ${Array.from(ranks.slice(offset, offset + 32)).join(", ")},`);
}
const source = `/* AUTO-GENERATED by scripts/generate-blue-noise.mjs. CC0-1.0. */\n` +
  `export const BLUE_NOISE_CHECKSUM = "${checksum}" as const;\n` +
  `export const BLUE_NOISE_RANKS = Uint16Array.of(\n${rows.join("\n")}\n);\n`;
const target = fileURLToPath(
  new URL("../lib/tiktok/blue-noise-ranks.generated.ts", import.meta.url),
);
writeFileSync(target, source, "utf8");
console.log(`${target}\n${checksum}\n${ranks.length} ranks`);
